package Calculator; // Mendeklarasikan bahwa kelas ini berada di dalam package bernama "Calculator"

public class Calculator {

    // Method untuk menjumlahkan dua angka
    // Parameter:
    //  - a: angka pertama
    //  - b: angka kedua
    // Return:
    //  - hasil penjumlahan dari a + b
    public int add(int a, int b) {
        return a + b; // Mengembalikan hasil penjumlahan
    }
}
